    private void reset(){
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        int row = model.getRowCount();
 //modul 3 perulangan for
        for(int i=row-1;i>=0;i--){           
            model.removeRow(i);
        };
    }
    
