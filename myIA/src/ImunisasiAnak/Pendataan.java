    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
//modul 1 tipe data dan variabel
        String n1 = "-",
               n2 = "-",
               n3 = "-",
               n4 = "-",
               n5 = "-",
               n6 = "-";
//modul 2 pengkondisian if else
//modul 4 fungsi dan method
        if(T1.isSelected()){
            n1 = "Sudah";
        } else if (F1.isSelected()){
            n1 = "Belum";
        }      
        if(T2.isSelected()){
            n2 = "Ya";
        } else if (F2.isSelected()){
            n2 = "Tidak";
        }
        if(T3.isSelected()){
            n3 = "Ya";
        } else if (F3.isSelected()){
            n3 = "Tidak";
        }
        if(T4.isSelected()){
            n4 = "Ya";
        } else if (F4.isSelected()){
            n4 = "Tidak";
        }
        if(T5.isSelected()){
            n5 = "Ya";
        } else if (F5.isSelected()){
            n5 = "Tidak";
        }
        if(T6.isSelected()){
            n6 = "Ya";
        } else if (F6.isSelected()){
            n6 = "Tidak";
        }
 
        if(n1 == "-" || n2 == "-" || n3 == "-" || n4 == "-" || n5 == "-" || n6 == "-"){
           JOptionPane.showMessageDialog(null, "Pastikan kolom terisi semua!"); 
        } else {
            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            model.addRow(new Object[]{i,n1,n2,n3,n4,n5,n6});
            this.i++;
            setClear();
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

